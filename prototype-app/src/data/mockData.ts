export function getLastWeightForExercise(exerciseId: string, workouts: WorkoutInstance[]): number | undefined {
  for (const workout of workouts) {
    const exercise = workout.exercises.find(e => e.exerciseId === exerciseId && e.weight);
    if (exercise?.weight) {
      return exercise.weight;
    }
  }
  return undefined;
}

export function getWorkoutsForDate(date: Date, workouts: WorkoutInstance[]): WorkoutInstance[] {
  const dateStr = date.toISOString().split('T')[0];
  return workouts.filter(w => {
    const workoutDate = new Date(w.startedAt).toISOString().split('T')[0];
    return workoutDate === dateStr;
  });
}

